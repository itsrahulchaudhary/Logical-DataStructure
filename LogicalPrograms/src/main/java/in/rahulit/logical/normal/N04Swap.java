package in.rahulit.logical.normal;

public class N04Swap {

	public static void main(String[] args) {
		int a = 10;
		int b = 20;
		System.out.println("Before Swap :: a = " + a + ", b = " + b);
		int t;
		t = a;
		a = b;
		b = t;
		System.out.println("After Swap :: a = " + a + ", b = " + b);

	}

}
